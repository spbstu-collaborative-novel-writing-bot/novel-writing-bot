CREATE TABLE IF NOT EXISTS app_users (
    chat_id BIGINT PRIMARY KEY,
    username VARCHAR(100),
    display_name VARCHAR(200),
    role VARCHAR(20) NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS novels (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(100) NOT NULL,
    description VARCHAR(1000) NOT NULL,
    genre VARCHAR(50) NOT NULL,
    owner_chat_id BIGINT NOT NULL REFERENCES app_users(chat_id) ON DELETE CASCADE,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS novel_authors (
    novel_id BIGINT NOT NULL REFERENCES novels(id) ON DELETE CASCADE,
    chat_id BIGINT NOT NULL REFERENCES app_users(chat_id) ON DELETE CASCADE,
    author_type VARCHAR(20) NOT NULL,
    PRIMARY KEY (novel_id, chat_id)
);

CREATE TABLE IF NOT EXISTS chapters (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    novel_id BIGINT NOT NULL REFERENCES novels(id) ON DELETE CASCADE,
    title VARCHAR(100) NOT NULL,
    text TEXT NOT NULL,
    order_number INTEGER NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    last_editor_chat_id BIGINT NOT NULL REFERENCES app_users(chat_id)
);

CREATE INDEX IF NOT EXISTS idx_chapters_novel_order ON chapters(novel_id, order_number);

CREATE TABLE IF NOT EXISTS chapter_history (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    chapter_id BIGINT NOT NULL REFERENCES chapters(id) ON DELETE CASCADE,
    old_title VARCHAR(100) NOT NULL,
    old_text TEXT NOT NULL,
    editor_chat_id BIGINT NOT NULL REFERENCES app_users(chat_id),
    changed_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_chapter_history_chapter ON chapter_history(chapter_id, changed_at DESC);

CREATE TABLE IF NOT EXISTS llm_requests (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    chat_id BIGINT NOT NULL REFERENCES app_users(chat_id) ON DELETE CASCADE,
    novel_id BIGINT NOT NULL REFERENCES novels(id) ON DELETE CASCADE,
    chapter_id BIGINT REFERENCES chapters(id) ON DELETE SET NULL,
    request_type VARCHAR(30) NOT NULL,
    status VARCHAR(20) NOT NULL,
    prompt TEXT NOT NULL,
    result TEXT,
    error_message TEXT,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

ALTER TABLE llm_requests ADD COLUMN IF NOT EXISTS provider VARCHAR(40) NOT NULL DEFAULT 'OPENAI_COMPATIBLE';
ALTER TABLE llm_requests ADD COLUMN IF NOT EXISTS model VARCHAR(120) NOT NULL DEFAULT '';
ALTER TABLE llm_requests ADD COLUMN IF NOT EXISTS completed_at TIMESTAMP;

CREATE TABLE IF NOT EXISTS telegram_sessions (
    chat_id BIGINT PRIMARY KEY REFERENCES app_users(chat_id) ON DELETE CASCADE,
    state VARCHAR(60) NOT NULL,
    novel_id BIGINT REFERENCES novels(id) ON DELETE CASCADE,
    chapter_id BIGINT REFERENCES chapters(id) ON DELETE CASCADE,
    payload TEXT NOT NULL DEFAULT '{}',
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
